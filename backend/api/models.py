    gateway = models.CharField(max_length=50)
    gateway_payment_id = models.CharField(max_length=255)
    platform_fee_pct = models.DecimalField(max_digits=5, decimal_places=4)
    platform_fee_amount = models.DecimalField(max_digits=10, decimal_places=2)
    consultant_payout_amount = models.DecimalField(max_digits=12, decimal_places=2)
    status = models.CharField(max_length=50, choices=[('pending', 'Pending'), ('paid', 'Paid'), ('failed', 'Failed'), ('refunded', 'Refunded')], default='pending')
    paid_at = models.DateTimeField(null=True, blank=True)
    refunded_at = models.DateTimeField(null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)

